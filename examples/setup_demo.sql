    ('Ель', 'yel', 'A002'),
    ('ångström', 'angstrom', 'A003'),
    ('apple', 'Apple', 'A004'),
    ('Äpfel', 'apfel', 'A005')
ON CONFLICT DO NOTHING;

CREATE INDEX IF NOT EXISTS sample_strings_name_idx
    ON pgcollcheck_demo.sample_strings (name);

CREATE INDEX IF NOT EXISTS sample_strings_code_c_idx
    ON pgcollcheck_demo.sample_strings (code COLLATE "C");
