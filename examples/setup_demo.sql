-- Optional demo objects for manual experiments.
-- The real checker does not require this file.

CREATE SCHEMA IF NOT EXISTS pgcollcheck_demo;

CREATE TABLE IF NOT EXISTS pgcollcheck_demo.sample_strings (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name text NOT NULL,
    nickname text NOT NULL,
    code text COLLATE "C" NOT NULL
);

INSERT INTO pgcollcheck_demo.sample_strings (name, nickname, code)
VALUES
